## ----looping_vector_examp------------------------------------------------
#We will assume vectors of the same length...
add_vecs <- function(vec1, vec2) {
  out<-NULL
  for(i in 1:length(vec1)) {
    out[i] <- vec1[i] + vec2[i]
  }
  out
}
add_vecs(x, y)

